num = int(input("Digite o número: "))

if(num == 1 ):
    print("Domingo! ")
elif(num == 2 ):
    print("Segunda-Feira! ")
elif(num == 3 ):
    print("Terça-Feira! ")
elif(num == 4 ):
    print("Quarta-Feira! ")
elif(num == 5 ):
    print("Quinta-Feira! ")
elif(num == 6 ):
    print("Sexta-Feira ")
elif(num == 7 ):
    print("Sábado! ")
else:
    print("Dia Invalído! ")